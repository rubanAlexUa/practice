/**
 * Інтерфейс черги завдань для Worker Thread.
 * Визначає чергу додавання та вилучення команд {@link WorkerCommand}.
 */
public interface Queue {
    /**
     * Додає команду до черги на виконання.
     *
     * @param cmd команда для додавання
     */
    void put(WorkerCommand cmd);

    /**
     * Вилучає та повертає наступну команду з черги.
     * Якщо черга порожня — очікує появи нової команди або завершення роботи.
     *
     * @return наступна команда або {@code null} якщо черга завершила роботу
     */
    WorkerCommand take();
}
